        document.addEventListener("DOMContentLoaded", function() {
            const btnsBefore = document.querySelectorAll(".btn-before");
            const btnsAfter = document.querySelectorAll(".btn-after");
            const beforeImgContainers = document.querySelectorAll(".before-img-container");
            const afterImgContainers = document.querySelectorAll(".after-img-container");
            const mainImgs = document.querySelectorAll(".main-img");
    
            // Function to show images associated with the "Before" button
            function showBeforeImages(beforeImgs, afterImgs, mainImg) {
                beforeImgs.forEach(img => img.classList.add("show"));
                afterImgs.forEach(img => img.classList.remove("show"));
            }
    
            // Function to show images associated with the "After" button
            function showAfterImages(beforeImgs, afterImgs, mainImg) {
                beforeImgs.forEach(img => img.classList.remove("show"));
                afterImgs.forEach(img => img.classList.add("show"));
            }
    
            // Click event listeners for the "Before" buttons
            btnsBefore.forEach((btnBefore, index) => {
                btnBefore.addEventListener("click", function() {
                    showBeforeImages(
                        beforeImgContainers[index].querySelectorAll(".before-img"),
                        afterImgContainers[index].querySelectorAll(".after-img"),
                        mainImgs[index]
                    );
                });
            });
    
            // Click event listeners for the "After" buttons
            btnsAfter.forEach((btnAfter, index) => {
                btnAfter.addEventListener("click", function() {
                    showAfterImages(
                        beforeImgContainers[index].querySelectorAll(".before-img"),
                        afterImgContainers[index].querySelectorAll(".after-img"),
                        mainImgs[index]
                    );
                });
            });
    
            // Trigger click event on the "After" buttons upon page load for each set
            btnsAfter.forEach((btnAfter, index) => {
                showAfterImages(
                    beforeImgContainers[index].querySelectorAll(".before-img"),
                    afterImgContainers[index].querySelectorAll(".after-img"),
                    mainImgs[index]
                );
            });
        });
